# todo: Operator ตัวดำเนินการ
'''
https://www.w3schools.com/python/python_operators.asp

Operator จะมี  2 ส่วนคือ
1. ตัวดำเนินการ = Operator
2. ตัวที่ถูกดำเนินการ = Operand
ยกตัวอย่าง
 A + B
 A, B ==> Operand
 + ==> Operator
'''
x = 10
y = 3
print('ผลบวก =', x + y)
print('ผลลบ =', x - y)
print('ผลคูณ =', x * y)
print('ผลหาร =', x / y)
print('หารปัดเศษ = ', x // y)
print('ยกกำลัง = ', x ** y)
print('หารเอาเศษ = ', x % y)  # 10 / 3  : 3*3 = 9 : เอา 10 - 9 จะได้ เศษ 1
